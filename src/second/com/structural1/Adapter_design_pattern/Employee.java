package second.com.structural1.Adapter_design_pattern;

public class Employee {

	private String fullName;
	private String jobTitle;
	private String officeLoaction;

	public String getFullName() {
		return fullName;
	}

	public void setFullname(String fullName) {
		this.fullName = fullName;
	}

	public String getJobTitle() {
		return jobTitle;
	}

	public void setJobTitle(String jobTitle) {
		this.jobTitle = jobTitle;
	}

	public String getOfficeLoaction() {
		return officeLoaction;
	}

	public void setOfficeLoaction(String officeLoaction) {
		this.officeLoaction = officeLoaction;
	}

}
